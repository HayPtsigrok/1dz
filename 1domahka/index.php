<?php

if ($_SERVER["REQUEST_METHOD"] == "POST") {
  // Проверяем, был ли запрос методом POST. Это значит, что форма была отправлена.

  $start_date = $_POST["start_date"];
  // Получаем значение даты начала из данных, отправленных через POST-запрос. 
  // Предполагается, что в форме есть поле с именем "start_date".

  $end_date = $_POST["end_date"];
  // Получаем значение даты окончания из данных, отправленных через POST-запрос.
  // Предполагается, что в форме есть поле с именем "end_date".

  $start_timestamp = strtotime(date("Y-m-d", strtotime($start_date)));
  // Преобразуем строку даты начала в формат временной метки (timestamp).
  // Сначала мы используем strtotime для преобразования строки в дату, затем форматируем ее в "Y-m-d" и снова преобразуем в timestamp.

  $end_timestamp = strtotime(date("Y-m-d", strtotime($end_date)));
  // То же самое делаем для даты окончания. Преобразуем строку даты окончания в временную метку.

  $days = ($end_timestamp - $start_timestamp) / (60 * 60 * 24);
  // Вычисляем количество дней между двумя датами.
  // Разница между временными метками делится на количество секунд в одном дне (60 секунд * 60 минут * 24 часа).

  $minutes = ($end_timestamp - $start_timestamp) / (60 * 60) * 60;
  // Вычисляем количество минут между двумя датами.
  // Разница между временными метками делится на количество секунд в одном часе (60 секунд * 60 минут) и затем умножается на 60, чтобы получить общее количество минут.

  echo "Количество дней между выбранными датами: " . round($days) . "; Количество минут между выбранными датами: " . round($minutes);
  // Выводим результаты на экран. Используем функцию round() для округления значений дней и минут.
}
